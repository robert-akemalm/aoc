    static class Tile {
        final int id;
        boolean[][] dots;
        int[] edges;

        Tile(int id, boolean[][] dots) {
            this.id = id;
            this.dots = dots;
            this.edges = new int[4];
            if (dots.length > 10) {
                return;
            }
            BitSet b = new BitSet();
            for (int i = 0; i < dots[0].length; i++) {
                if (dots[0][i]) {
                    b.set(i);
                }
            }
            edges[0] = (int) b.toLongArray()[0];

            b.clear();
            for (int i = 0; i < dots[0].length; i++) {
                if (dots[i][9]) {
                    b.set(i);
                }
            }
            edges[1] = (int) b.toLongArray()[0];

            b.clear();
            for (int i = 0; i < dots[0].length; i++) {
                if (dots[9][i]) {
                    b.set(9 - i);
                }
            }
            edges[2] = (int) b.toLongArray()[0];

            b.clear();
            for (int i = 0; i < dots[0].length; i++) {
                if (dots[i][0]) {
                    b.set(9 - i);
                }
            }
            edges[3] = (int) b.toLongArray()[0];
        }

        static Tile parse(String s) {
            String[] rows = s.split("\n");
            boolean[][] dots = IntStream.range(0, 10).mapToObj(i -> new boolean[10]).toArray(boolean[][]::new);
            for (int row = 1; row < rows.length; row++) {
                for (int col = 0; col < 10; col++) {
                    dots[row - 1][col] = rows[row].charAt(col) == '.';
                }
            }
            return new Tile(Integer.parseInt(rows[0].substring(5, rows[0].length() - 1)), dots);
        }

        int[] matches() {
            return Arrays.stream(edges).map(e -> {
                BitSet bitSet = BitSet.valueOf(new long[] {e});
                BitSet a = new BitSet();
                for (int i = 0; i < 10; i++) {
                    if (bitSet.get(i)) {
                        a.set(9 - i);
                    }
                }
                return ((int) a.toLongArray()[0]);
            }).toArray();
        }

        @Override
        public String toString() {
            return "" + id;
        }

        public void turn() {
            edges = new int[] {edges[3], edges[0], edges[1], edges[2]};
            boolean[][] dots = IntStream.range(0, this.dots.length).mapToObj(i -> new boolean[this.dots.length]).toArray(boolean[][]::new);
            for (int row = 0; row < this.dots.length; row++) {
                for (int col = 0; col < this.dots.length; col++) {
                    dots[col][this.dots.length - 1 - row] = this.dots[row][col];
                }
            }
            this.dots = dots;
        }

        public void flip() {
            edges = new int[] {edges[0], edges[3], edges[2], edges[1]};
            boolean[][] dots = IntStream.range(0, this.dots.length).mapToObj(i -> new boolean[this.dots.length]).toArray(boolean[][]::new);
            for (int row = 0; row < this.dots.length; row++) {
                for (int col = 0; col < this.dots.length; col++) {
                    dots[row][col] = this.dots[row][this.dots.length - 1 - col];
                }
            }
            this.dots = dots;
        }
    }

