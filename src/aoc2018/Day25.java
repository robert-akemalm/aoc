    private static void a(String input) {
        Point[] points = Stream.of(Util.parseStrings(input)).map(Point::parse).toArray(Point[]::new);
        List<Constellation> constellations = new ArrayList<>();
        for (Point p : points) {
            boolean added = false;
            for (Constellation constellation : constellations) {
                if (constellation.inRange(p)) {
                    constellation.points.add(p);
                    added = true;
                    break;
                }
            }
            if (!added) {
                constellations.add(new Constellation(p));
            }
        }

        int previousNrOfConstellations = 0;
        while (previousNrOfConstellations != constellations.size()) {
            System.out.println(constellations.size());
            previousNrOfConstellations = constellations.size();
            for (int i = 0; i < constellations.size(); i++) {
                Constellation c = constellations.get(i);
                for (int j = constellations.size()-1; j > i ; j--) {
                    Constellation cj = constellations.get(j);
                    if (c.inRange(cj)) {
                        c.points.addAll(cj.points);
                        constellations.remove(j);
                    }
                }
            }
        }
    }

    private static class Constellation {
        final Set<Point> points = new HashSet<>();

        public Constellation(Point p) {
            points.add(p);
        }

        boolean inRange(Point point) {
            for (Point p : points) {
                if (point.distance(p) <= 3) {
                    return true;
                }
            }
            return false;
        }

        boolean inRange(Constellation constellation) {
            for (Point p : points) {
                for (Point c : constellation.points) {
                    if (p.distance(c) <= 3) {
                        return true;
                    }
                }
            }
            return false;
        }
    }

    private static class Point {
        final int x;
        final int y;
        final int z;
        final int t;

        private Point(int x, int y, int z, int t) {
            this.x = x;
            this.y = y;
            this.z = z;
            this.t = t;
        }

        int distance(Point p) {
            return Math.abs(x - p.x) + Math.abs(y - p.y) + Math.abs(z - p.z) + Math.abs(t - p.t);
        }

        static Point parse(String data) {
            String[] xyzt = data.split(",");
            int x = Integer.parseInt(xyzt[0]);
            int y = Integer.parseInt(xyzt[1]);
            int z = Integer.parseInt(xyzt[2]);
            int t = Integer.parseInt(xyzt[3]);
            return new Point(x, y, z, t);
        }
    }

