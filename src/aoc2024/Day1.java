    private static void a(Input input) {
        input.left.sort(Integer::compareTo);
        input.right.sort(Integer::compareTo);
        int sum = IntStream.range(0, input.left.size())
                           .map(i -> Math.abs(input.left.get(i) - input.right.get(i)))
                           .sum();
        System.out.println(sum);
    }

    private static void b(Input input) {
        Map<Integer, Integer> rightToCount = new HashMap<>();
        input.right.forEach(i -> rightToCount.merge(i, 1, Integer::sum));
        System.out.println(input.left.stream()
                                     .mapToInt(i -> rightToCount.getOrDefault(i, 0) * i)
                                     .sum());
    }


