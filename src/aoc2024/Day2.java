    record Level(int[] values) {
        boolean safe() {
            boolean asc = values[0] < values[1];
            for (int i = 0; i < values.length - 1; i++) {
                if (asc ? values[i] >= values[i + 1] : values[i] <= values[i + 1]) {
                    return false;
                }
                if (Math.abs(values[i] - values[i + 1]) > 3 || values[i] == values[i + 1]) {
                    return false;
                }
            }
            return true;
        }

        boolean safeWithDamper() {
            return IntStream.range(0, values.length)
                            .mapToObj(i -> IntStream.range(0, values.length)
                                                    .filter(j -> j != i)
                                                    .map(j -> values[j])
                                                    .toArray())
                            .map(Level::new)
                            .anyMatch(Level::safe);
        }
    }

    record Input(List<Level> levels) {
        static Input parse(String input) {
            List<Level> levels = input.lines()
                                      .map(l -> l.split(" "))
                                      .map(a -> Arrays.stream(a).mapToInt(Integer::parseInt).toArray())
                                      .map(Level::new)
                                      .toList();
            return new Input(levels);
        }
    }

