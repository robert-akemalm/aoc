    private record State(Pos pos, int steps) {}

    private static void a(Input input) {
        boolean isTestInput = input.bytes.getFirst().x() == 5;
        System.out.println(minStepsToExit(input, isTestInput ? 6 : 70, isTestInput ? 12 : 1024));
    }

    private static void b(Input input) {
        int max = input.bytes.getFirst().x() == 5 ? 6 : 70;
        for (int numBytes = input.bytes.size(); numBytes >= 0; numBytes--) {
            if (minStepsToExit(input, max, numBytes) != -1) {
                System.out.println(input.bytes.get(numBytes));
                return;
            }
        }
    }
    
    private static int minStepsToExit(Input input, int gridSize, int numBytes) {
        Set<Pos> blocked = input.bytes().stream().limit(numBytes).collect(Collectors.toSet());
        PriorityQueue<State> toExplore = new PriorityQueue<>(Comparator.comparingInt(State::steps));
        toExplore.add(new State(new Pos(0, 0), 0));
        Set<Pos> seen = new HashSet<>();
        while (!toExplore.isEmpty()) {
            State state = toExplore.poll();
            if (state.pos.y() == gridSize && state.pos.x() == gridSize) {
                return state.steps();
            }
            if (seen.add(state.pos())) {
                state.pos.neighbours().stream()
                         .filter(n -> 0 <= n.x() && n.x() <= gridSize)
                         .filter(n -> 0 <= n.y() && n.y() <= gridSize)
                         .filter(n -> !blocked.contains(n))
                         .forEach(n -> toExplore.add(new State(n, state.steps() + 1)));
            }
        }
        return -1;
    }


    record Input(List<Pos> bytes) {
        static Input parse(String input) {
            return new Input(input.lines().map(Util::extractInts).map(a -> new Pos(a[0], a[1])).toList());
        }
    }

