    private static void b(String input) {
        System.out.println(input.lines().filter(line ->{
            int min = Integer.parseInt(line.substring(0, line.indexOf('-')));
            int max = Integer.parseInt(line.substring(line.indexOf('-') + 1, line.indexOf(' ')));
            char c = line.charAt(line.indexOf(':') - 1);
            String pwd = line.substring(line.indexOf(':') + 1);
            return pwd.charAt(min) == c ^ pwd.charAt(max) == c;
        }).count());
    }

    private static void a(String input) {
        System.out.println(input.lines().filter(line ->{
            int min = Integer.parseInt(line.substring(0, line.indexOf('-')));
            int max = Integer.parseInt(line.substring(line.indexOf('-') + 1, line.indexOf(' ')));
            char c = line.charAt(line.indexOf(':') - 1);
            String pwd = line.substring(line.indexOf(':') + 2);
            int cnt = (int) pwd.chars().filter(ch -> ch == c).count();
            return min <= cnt && cnt <= max;
        }).count());
    }

