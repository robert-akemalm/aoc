    static class Path {
        final Set<Pos> visited = new HashSet<>();
        List<Node> debug = new ArrayList<>();
        Node current = null;
        Node previous = null;
        int steps;
        final int level;

        @Override
        public String toString() {
            Path p = null;
            List<String> rows = new ArrayList<>();
            List<Node> row = new ArrayList<>();
            for (int i = 0; i < debug.size(); i++) {
                if (p == null) {
                    p = new Path(debug.get(i));
                } else {
                    p = new Path(p, debug.get(i));
                }
                row.add(debug.get(i));
                if (debug.get(i).portal != null) {
                    if (!row.isEmpty()) {
                        steps += p.steps;
                        rows.add(row.toString() + " - L" + p.level + " - " + p.steps);
                        row.clear();
                    }
                }
            }
            if (!row.isEmpty()) {
                rows.add(row.toString() + " - L" + p.level + " - " + p.steps);
            }
            return String.join("\n", rows);
        }

        public Path(Path path, Node current) {
                Edge e = path.current.edges.stream().filter(edge -> edge.node == current).findAny().get();
                this.steps = path.steps + e.steps;
                this.visited.addAll(path.visited);
                this.visited.add(current.pos);
                this.debug.addAll(path.debug);
                this.debug.add(current);
                this.previous = path.current;
                int levelModifier = Objects.equals(current.portal, path.current.portal) ? 0 : current.levelModifier;
                this.level = path.level + levelModifier;
                this.current = current;
            }

        public Path(Node start) {
                this.visited.add(start.pos);
                this.debug.add(start);
                this.current = start;
                this.steps = 0;
                this.level = 0;
            }

            public List<Path> possibleSteps () {
                List<Path> possible = new ArrayList<>();
                Node c = current;
                for (Edge edge : c.edges) {
                    Node next = edge.node;
                    if (next != previous) {
                        if (level != 0 && !"AA".equals(next.portal) && !"ZZ".equals(next.portal)) {
                            possible.add(new Path(this, next));
                        } else if (level == 0 && (next.levelModifier < 1 || "ZZ".equals(next.portal))){
                            possible.add(new Path(this, next));
                        }
                    }
                }
                return possible;
            }
        }

        private static boolean reduce() {
            Pos toRemove = null;
            for (Entry<Pos, Node> e : map.entrySet()) {
                Node node = e.getValue();
                if (node.edges.size() == 2 && node.symbol == '.') {
                    toRemove = e.getKey();
                    Node a = node.edges.get(0).node;
                    Node b = node.edges.get(1).node;
                    int steps = node.edges.get(0).steps + node.edges.get(1).steps;
                    for (int i = 0; i < a.edges.size(); i++) {
                        if (a.edges.get(i).node.pos == node.pos) {
                            a.edges.set(i, new Edge(b, steps));
                        }
                    }
                    for (int i = 0; i < b.edges.size(); i++) {
                        if (b.edges.get(i).node.pos == node.pos) {
                            b.edges.set(i, new Edge(a, steps));
                        }
                    }
                    break;
                }
            }
            if (toRemove != null) {
                map.remove(toRemove);
            }
            return toRemove != null;
        }

