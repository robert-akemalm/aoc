    private static void b(String input) {
        String[] lines = input.split("\n");
        String start = "";
        for (int i = 0; i < lines[0].length(); i++) {
            int numOnes = 0;
            int numZeros = 0;
            for (String line : lines) {
                if (line.charAt(i) == '0') {
                    numZeros++;
                } else {
                    numOnes++;
                }
            }
            if (numOnes >= numZeros) {
                start += "1";
            } else {
                start += "0";
            }
            final String prefix = start;
            lines = Arrays.stream(lines).filter(l -> l.startsWith(prefix)).toArray(String[]::new);
            if (lines.length == 1) {
                break;
            }
        }
        int g = Integer.parseInt(lines[0], 2);
        lines = input.split("\n");
        start = "";
        for (int i = 0; i < lines[0].length(); i++) {
            int numOnes = 0;
            int numZeros = 0;
            for (String line : lines) {
                if (line.charAt(i) == '0') {
                    numZeros++;
                } else {
                    numOnes++;
                }
            }
            if (numZeros <= numOnes) {
                start += "0";
            } else {
                start += "1";
            }
            final String prefix = start;
            lines = Arrays.stream(lines).filter(l -> l.startsWith(prefix)).toArray(String[]::new);
            if (lines.length == 1) {
                break;
            }
        }
        int e = Integer.parseInt(lines[0], 2);
        System.out.println(g + " * " + e + " = " + (g*e));
    }

    private static void a(String input) {
        String[] lines = input.split("\n");
        String gamma = "";
        String epsilon = "";
        for (int i = 0; i < lines[0].length(); i++) {
            int numOnes = 0;
            int numZeros = 0;
            for (String line : lines) {
                if (line.charAt(i) == '0') {
                    numZeros++;
                } else {
                    numOnes++;
                }
            }
            gamma += numOnes > numZeros ? "1" : "0";
            epsilon += numOnes < numZeros ? "1" : "0";
        }
        System.out.println(gamma + " " + epsilon);
        int g = Integer.parseInt(gamma, 2);
        int e = Integer.parseInt(epsilon, 2);
        System.out.println(g + " * " + e + " = " + (g*e));
    }

