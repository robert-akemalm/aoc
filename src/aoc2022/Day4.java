    private record Pair(Set<Integer> a, Set<Integer> b) {
        boolean fullyContained() {
            return a.containsAll(b) || b.containsAll(a);
        }

        boolean overlap() {
            return new HashSet<>(a).removeAll(b);
        }

        static Pair parse(String line) {
            String[] parts = line.split(",");
            return new Pair(parseRange(parts[0]), parseRange(parts[1]));
        }

        static Set<Integer> parseRange(String data) {
            String[] parts = data.split("-");
            return IntStream.rangeClosed(Integer.parseInt(parts[0]), Integer.parseInt(parts[1]))
                            .boxed()
                            .collect(Collectors.toSet());
        }
    }

