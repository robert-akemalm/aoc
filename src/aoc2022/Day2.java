    private static void b(String input) {
        System.out.println(input.lines().map(Round::parse).mapToInt(Round::score2).sum());
    }

    private static void a(String input) {
        System.out.println(input.lines().map(Round::parse).mapToInt(Round::score).sum());
    }

    record Round(ABC col1, XYZ col2) {
        static Round parse(String input) {
            String[] parts = input.split(" ");
            return new Round(ABC.valueOf(parts[0]), XYZ.valueOf(parts[1]));
        }

        int score() {
            Selection opponent = col1.selection;
            Selection me = col2.selection;
            Result result = Result.compare(me, opponent);
            return result.score + me.score;
        }

        int score2() {
            Selection opponent = col1.selection;
            Result result = col2.result;
            Selection me = result.against(opponent);
            return result.score + me.score;
        }
    }

    private enum Selection {
        ROCK(1), PAPER(2), SCISSOR(3);

        final int score;

        Selection(int score) {
            this.score = score;
        }
    }

    private enum Result {
        WIN(6), DRAW(3), LOOSE(0);

        final int score;

        Result(int score) {
            this.score = score;
        }

        static Result compare(Selection me, Selection opponent) {
            if (me == opponent) {
                return DRAW;
            } else if (me == Selection.ROCK) {
                return opponent == Selection.SCISSOR ? WIN : LOOSE;
            } else if (me == Selection.PAPER) {
                return opponent == Selection.ROCK ? WIN : LOOSE;
            } else {
                return opponent == Selection.PAPER ? WIN : LOOSE;
            }
        }

        Selection against(Selection opponent) {
            for (Selection me : Selection.values()) {
                if (this == compare(me, opponent)) {
                    return me;
                }
            }
            throw new RuntimeException();
        }
    }

    private enum ABC {
        A(Selection.ROCK), B(Selection.PAPER), C(Selection.SCISSOR);

        final Selection selection;

        ABC(Selection selection) {
            this.selection = selection;
        }
    }

    private enum XYZ {
        X(Selection.ROCK, Result.LOOSE), Y(Selection.PAPER, Result.DRAW), Z(Selection.SCISSOR, Result.WIN);

        final Selection selection;
        final Result result;

        XYZ(Selection selection, Result result) {
            this.selection = selection;
            this.result = result;
        }
    }

