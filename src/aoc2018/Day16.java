    private static class Register {
        final int[] registers;

        @Override
        public String toString() {
            return Arrays.toString(registers);
        }

        private Register(int[] registers) {
            this.registers = registers;
        }

        static Register parse(String data) {
            String[] parts = data.substring(data.indexOf('[') + 1, data.indexOf(']')).split(", ");
            int[] registers = Stream.of(parts).mapToInt(Integer::parseInt).toArray();
            return new Register(registers);
        }
    }

    private static class Example {
        final Register input;
        final int[] instruction;
        final Register output;

        private Example(Register input, int[] instruction, Register output) {
            this.input = input;
            this.instruction = instruction;
            this.output = output;
        }

        static Example parse(String[] rows) {
            if (rows.length != 3) {
                return null;
            }
            Register input = Register.parse(rows[0]);
            int[] instruction = Stream.of(rows[1].split(" ")).mapToInt(Integer::parseInt).toArray();
            Register output = Register.parse(rows[2]);
            return new Example(input, instruction, output);
        }
    }

    private static class OpTester {
        final Register testRegister = new Register(new int[4]);

        int test(Example example, List<Operation> operations) {
            int matchingOperations = 0;
            for(Operation operation : operations) {
                System.arraycopy(example.input.registers, 0, testRegister.registers, 0, example.input.registers.length);
                int[] instruction = example.instruction;
                operation.run(testRegister, instruction[1], instruction[2], instruction[3]);
                if (Arrays.equals(example.output.registers, testRegister.registers)) {
                    matchingOperations++;
                }
            }
            return matchingOperations;
        }
    }

    private static class OpMapper {
        final Register testRegister = new Register(new int[4]);
        final Set<Operation>[] possibleOps;

        private OpMapper(List<Operation> possible) {
            this.possibleOps = new Set[possible.size()];
            for (int i = 0; i < possibleOps.length; i++) {
                possibleOps[i] = new HashSet<>(possible);
            }
        }

        void test(Example example) {
            int[] instruction = example.instruction;
            Set<Operation> possible = possibleOps[instruction[0]];
            for(Operation op : possible.toArray(new Operation[0])) {
                System.arraycopy(example.input.registers, 0, testRegister.registers, 0, example.input.registers.length);
                op.run(testRegister, instruction[1], instruction[2], instruction[3]);
                if (!Arrays.equals(example.output.registers, testRegister.registers)) {
                    possible.remove(op);
                }
            }
        }

        public Operation[] get() {
            Operation[] ops = new Operation[possibleOps.length];
            for (int k = 0; k < possibleOps.length; k++) {
                for (int i = 0; i < possibleOps.length; i++) {
                    Set<Operation> o = possibleOps[i];
                    if (o.size() == 1) {
                        ops[i] = o.iterator().next();
                        for (int j = 0; j < possibleOps.length; j++) {
                            possibleOps[j].remove(ops[i]);
                        }
                    }
                }
            }
            return ops;
        }
    }
