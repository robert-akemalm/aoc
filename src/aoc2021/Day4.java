    private static class Board {
        final int[][] numbers;
        final boolean[][] marked;
        boolean done = false;

        private Board(String[] lines) {
            numbers = new int[lines.length][];
            marked = new boolean[lines.length][];
            for (int i = 0; i < lines.length; i++) {
                numbers[i] = Arrays.stream(lines[i].trim().split(" ")).filter(s-> !s.isEmpty()).mapToInt(Integer::parseInt).toArray();
                marked[i] = new boolean[numbers[i].length];
            }
        }

        boolean mark(int number) {
            for (int i = 0; i < numbers.length; i++) {
                for (int j = 0; j < numbers[i].length; j++) {
                    if (numbers[i][j] == number) {
                        marked[i][j] = true;
                        done |= bingoOnRow(i) || bingoOnCol(j);
                    }
                }
            }
            return done;
        }

        boolean bingoOnRow(int i) {
            for (boolean m : marked[i]) {
                if (!m) {
                    return false;
                }
            }
            return true;
        }

        boolean bingoOnCol(int i) {
            for (boolean[] m : marked) {
                if (!m[i]) {
                    return false;
                }
            }
            return true;
        }
    }

