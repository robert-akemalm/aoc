    static boolean noCommonEdge(int aEdge, Tile a, Tile[] tiles) {
        return Arrays.stream(tiles).filter(t -> t != a).noneMatch(t -> hasCommonEdge(aEdge, t));
    }

    static boolean hasCommonEdge(Tile a, Tile b) {
        for (int aEdge : a.edges) {
            for (int bEdge : b.edges) {
                if (aEdge == bEdge) {
                    return true;
                }
            }
            for (int bEdge : b.matches()) {
                if (aEdge == bEdge) {
                    return true;
                }
            }
        }
        return false;
    }

    static boolean hasCommonEdge(int aEdge, Tile b) {
        for (int bEdge : b.edges) {
            if (aEdge == bEdge) {
                return true;
            }
        }
        for (int bEdge : b.matches()) {
            if (aEdge == bEdge) {
                return true;
            }
        }
        return false;
    }

