    private static void b(Input input) {
        Map<String, Set<String>> computerToConnections = new HashMap<>();
        for (Connection c : input.connections) {
            computerToConnections.computeIfAbsent(c.a, k -> new HashSet<>()).add(c.b);
            computerToConnections.computeIfAbsent(c.b, k -> new HashSet<>()).add(c.a);
        }
        Map<String, Set<String>> computerToAllConnections = new HashMap<>();
        for (Connection c : input.connections) {
            computerToAllConnections.computeIfAbsent(c.a, k -> new HashSet<>()).addAll(List.of(c.a, c.b));
            computerToAllConnections.computeIfAbsent(c.b, k -> new HashSet<>()).addAll(List.of(c.a, c.b));
        }

        int maxSize = computerToAllConnections.values().stream().mapToInt(Set::size).max().orElseThrow();
        for (int i = maxSize; i > 0; i--) {
            int finalMaxSize = i;
            List<Set<String>> groups = computerToAllConnections.values().stream()
                                                               .distinct()
                                                               .flatMap(s -> getSubsetsOfSize(s, finalMaxSize).stream())
                                                               .distinct()
                                                               .toList();
            Set<String> max = null;
            for (Set<String> group : groups) {
                boolean match = true;
                for (String c1 : group) {
                    for (String c2 : group) {
                        if (!computerToAllConnections.get(c1).contains(c2)) {
                            match = false;
                            break;
                        }
                    }
                    if (!match) {
                        break;
                    }
                }
                if (match) {
                    max = group;
                    break;
                }
            }
            if (max != null) {
                System.out.println(max.stream().sorted().collect(Collectors.joining(",")));
                break;
            }
            System.out.println("i: " + i);
        }
    }

    // Method to generate subsets of a given size
    public static <T> List<Set<T>> getSubsetsOfSize(Set<T> originalSet, int subsetSize) {
        List<T> list = new ArrayList<>(originalSet);

        // Generate subsets and filter by size
        return IntStream.range(0, 1 << list.size())
                        .mapToObj(i -> IntStream.range(0, list.size())
                                                .filter(j -> (i & (1 << j)) > 0)
                                                .mapToObj(list::get)
                                                .collect(Collectors.toSet()))
                        .filter(subset -> subset.size() == subsetSize) // Filter subsets by size
                        .collect(Collectors.toList());
    }
    record Connection(String a, String b) {
        static Connection parse(String line) {
            String[] parts = line.split("-");
            return new Connection(parts[0], parts[1]);
        }
    }

    record Input(List<Connection> connections) {
        static Input parse(String input) {
            return new Input(input.lines().map(Connection::parse).toList());
        }
    }

