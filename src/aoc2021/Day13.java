    record Dot(int y, int x) {
        static Dot parse(String s) {
            String[] parts = s.split(",");
            return new Dot(Integer.parseInt(parts[1]), Integer.parseInt(parts[0]));
        }
    }
    private static void a(String input) {
        Set<Dot> dots = input.lines().filter(l-> l.contains(",")).map(Dot::parse).collect(Collectors.toSet());
        List<String> folds = input.lines().filter(l->l.startsWith("fold")).toList();
        for (String fold : folds) {
            int val = Integer.parseInt(fold.substring(fold.indexOf('=') + 1));
            if (fold.contains("x=")) {
                dots = foldX(dots, val);
            } else {
                dots = foldY(dots, val);
            }
            System.out.println(dots.size());
        }
        List<Pos> pos = dots.stream().map(d -> new Pos(d.x, d.y)).toList();
        Util.print(pos);
        System.out.println();
    }

    static Set<Dot> foldY(Set<Dot> dots, int y) {
        Set<Dot> newDots = new HashSet<>();
        for (Dot dot : dots) {
            if (dot.y < y) {
                newDots.add(dot);
            } else {
                int newY = y - (dot.y - y);
                newDots.add(new Dot(newY, dot.x));
            }
        }
        return newDots;
    }

    static Set<Dot> foldX(Set<Dot> dots, int x) {
        Set<Dot> newDots = new HashSet<>();
        for (Dot dot : dots) {
            if (dot.x < x) {
                newDots.add(dot);
            } else {
                int newX = x - (dot.x - x);
                newDots.add(new Dot(dot.y, newX));
            }
        }
        return newDots;
    }

