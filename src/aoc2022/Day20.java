    private static class Data {
        final int length;
        final Map<Integer, Node> originalIxToNode = new HashMap<>();
        Node first;
        Node last;

        public Data(long[] data) {
            this.length = data.length;
            Node prev = new Node(-1, -1);
            for (int i = 0; i < length; i++) {
                Node node = new Node(i, data[i]);
                prev.next = node;
                node.prev = prev;
                prev = node;
                originalIxToNode.put(i, node);
            }

            this.first = originalIxToNode.get(0);
            this.last = originalIxToNode.get(length-1);
            this.first.prev = this.last;
            this.last.next = first;
        }

        void shuffle() {
            Node firstAfterShuffle = first;
            Node lastAfterShuffle = last;
            for (int i = 0; i < length; i++) {
                Node node = originalIxToNode.get(i);
                int move = (int) (node.val % (length-1));
                if (move==0) {
                    continue;
                }
                node.prev.next = node.next;
                node.next.prev = node.prev;
                if (move < 0) {
                    if (node == lastAfterShuffle) {
                        lastAfterShuffle = node.prev;
                    }
                    Node prevAfterMove = node.prev;
                    while (move != 0) {
                        prevAfterMove = prevAfterMove.prev;
                        move++;
                    }
                    node.prev = prevAfterMove;
                    node.next = prevAfterMove.next;
                    node.prev.next = node;
                    node.next.prev = node;
                    if (node.next == firstAfterShuffle) {
                        lastAfterShuffle = node;
                    }
                } else {
                    if (node == firstAfterShuffle) {
                        firstAfterShuffle = node.next;
                    }
                    Node nextAfterMove = node.next;
                    while (move != 0) {
                        nextAfterMove = nextAfterMove.next;
                        move--;
                    }
                    node.next = nextAfterMove;
                    node.prev = nextAfterMove.prev;
                    node.prev.next = node;
                    node.next.prev = node;
                    if (node.prev == lastAfterShuffle) {
                        firstAfterShuffle = node;
                    }
                }
            }
            first = firstAfterShuffle;
            last = lastAfterShuffle;
        }

        int indexOf(int value) {
            Node node = first;
            int ix = 0;
            while (node.val != value) {
                node = node.next;
                ix++;
            }
            return ix;
        }

        long getAt(int ix) {
            ix = ix % length;
            Node node = first;
            for (int i = 0; i < ix; i++) {
                node = node.next;
            }
            return node.val;
        }
    }

    private static class Node {
        final int originalIx;
        final long val;
        Node prev;
        Node next;

        private Node(int originalIx, long val) {
            this.originalIx = originalIx;
            this.val = val;
        }

        @Override
        public boolean equals(Object o) {
            return this == o;
        }

        @Override
        public int hashCode() {
            return originalIx;
        }
    }

