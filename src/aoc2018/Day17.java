    static class Ground {
        static final int CLAY = -1;
        static final int WATER = 1;
        static final int STILL_WATER = 2;
        final int[][] tiles;
        final int minX;
        final int maxX;
        final int minY;
        final int maxY;

        Ground(List<Point> clay) {
            minX = clay.stream().mapToInt(p -> p.x).min().getAsInt();
            maxX = clay.stream().mapToInt(p -> p.x).max().getAsInt();
            minY = clay.stream().mapToInt(p -> p.y).min().getAsInt();
            maxY = clay.stream().mapToInt(p -> p.y).max().getAsInt();
            tiles = new int[maxX + 3][];
            for (int x = 0; x < tiles.length; x++) {
                tiles[x] = new int[maxY + 2];
            }
            for (Point p : clay) {
                tiles[p.x][p.y] = CLAY;
            }
        }

        void print(int startX, int endX, int startY, int endY) {
            StringBuilder sb = new StringBuilder();
            for (int y = startY; y <= endY; y++) {
                for (int x = startX; x <= endX; x++) {
                    int tile = tiles[x][y];
                    switch (tile) {
                    case CLAY:
                        sb.append('#');
                        break;
                    case WATER:
                        sb.append('|');
                        break;
                    case STILL_WATER:
                        sb.append('~');
                        break;
                    default:
                        sb.append('.');
                    }
                }
                sb.append('\n');
            }
            System.out.println(sb);
            System.out.println();
        }

        boolean fill(int x, int y) {
            if (tiles[x][y] == WATER || y > maxY) {
                return false;
            }
            if (tiles[x][y] == CLAY || tiles[x][y] == STILL_WATER) {
                return true;
            }
            tiles[x][y] = WATER;
            boolean full = fill(x, y + 1);
            checkStill(x, y);
            if (full) {
                fill(x - 1, y);
                checkStill(x, y);
                fill(x + 1, y);
            }
            checkStill(x, y);
            return tiles[x][y] == STILL_WATER;
        }

        void checkStill(int x, int y) {
            if (tiles[x][y] != WATER) {
                return;
            }
            if (tiles[x][y + 1] == CLAY || tiles[x][y + 1] == STILL_WATER) {
                boolean isStill = true;
                for (int i = x - 1 ; i > 0 ; i--) {
                    int tile = tiles[i][y];
                    if (tile == STILL_WATER || tile == CLAY) {
                        break;
                    }
                    int tileBelow = tiles[i][y + 1];
                    if (tileBelow != STILL_WATER && tileBelow != CLAY) {
                        isStill = false;
                        break;
                    }
                }
                for (int i = x + 1 ; i < tiles.length ; i++) {
                    int tile = tiles[i][y];
                    if (tile == STILL_WATER || tile == CLAY) {
                        break;
                    }
                    int tileBelow = tiles[i][y + 1];
                    if (tileBelow != STILL_WATER && tileBelow != CLAY) {
                        isStill = false;
                        break;
                    }
                }
                if (isStill) {
                    tiles[x][y] = STILL_WATER;
                    checkStill(x-1, y);
                    checkStill(x+1, y);
                } else {
//                    print(450, 500, 80,100);
                }
            }

        }
        int countWater() {
            int sum = 0;
            for (int x = 0; x < tiles.length; x++) {
                for (int y = minY; y <= maxY; y++) {
                    if (tiles[x][y] == WATER || tiles[x][y] == STILL_WATER) {
                        sum++;
                    }
                }
            }
            return sum;
        }
        int countStillWater() {
            int sum = 0;
            for (int x = 0; x < tiles.length; x++) {
                for (int y = minY; y < maxY; y++) {
                    if (tiles[x][y] == STILL_WATER) {
                        sum++;
                    }
                }
            }
            return sum;
        }
    }

    static List<Point> parse(String data) {
        String[] parts = data.split(", ");
        boolean firstIsX = parts[0].startsWith("x");
        List<Integer> firstValues = parseValues(parts[0]);
        List<Integer> secondValues = parseValues(parts[1]);
        List<Point> points = new ArrayList<>();
        for (Integer a : firstValues) {
            for (Integer b : secondValues) {
                points.add(firstIsX ? new Point(a, b) : new Point(b, a));
            }
        }
        return points;
    }

    static List<Integer> parseValues(String data) {
        if (data.contains(".")) {
            int start = Integer.parseInt(data.substring(2, data.indexOf('.')));
            int end = Integer.parseInt(data.substring(data.lastIndexOf('.') + 1));
            List<Integer> values = new ArrayList<>();
            for (int i = start; i <= end; i++) {
                values.add(i);
            }
            return values;
        } else {
            return Collections.singletonList(Integer.parseInt(data.substring(2)));
        }
    }

    private static class Point {
        final int x;
        final int y;

        private Point(int x, int y) {
            this.x = x;
            this.y = y;
        }
    }

