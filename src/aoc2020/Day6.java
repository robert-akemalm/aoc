    private static void b(String input) {
        String[] groups = input.split("\n\n");
        int cnt = 0;
        for (String group : groups) {
            String[] persons = group.lines().toArray(String[]::new);
            BitSet groupSet = null;
            for (String person : persons) {
                BitSet p = new BitSet();
                person.chars().forEach(p::set);
                if (groupSet == null) {
                    groupSet = p;
                } else {
                    groupSet.and(p);
                }
            }
            cnt += groupSet.cardinality();
        }
        System.out.println(cnt);

    }

    private static void a(String input) {
        String[] groups = input.split("\n\n");
        int cnt = 0;
        for (String group : groups) {
            BitSet bitSet = new BitSet();
            group.chars().filter(c -> 'a' <= c && c <= 'z').forEach(bitSet::set);
            cnt += bitSet.cardinality();
        }
        System.out.println(cnt);
    }

