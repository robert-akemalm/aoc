    private static void b(String input) {
        Data data = Data.parse(input);
        data.nameToEvaluated.remove("humn");
        Expression root = data.nameToExpression.remove("root");
        data.reduceRHS();
        if (data.nameToEvaluated.containsKey(root.a)) {
            long humn = data.reduceLHS(root.b, data.nameToEvaluated.get(root.a));
            System.out.println(humn);
        } else {
            long humn = data.reduceLHS(root.a, data.nameToEvaluated.get(root.b));
            System.out.println(humn);
        }
    }

    private enum Operation {
        ADD((a, b) -> a + b, (c, b) -> c - b, (c, a) -> c - a),
        SUB((a, b) -> a - b, (c, b) -> c + b, (c, a) -> a - c),
        MUL((a, b) -> a * b, (c, b) -> c / b, (c, a) -> c / a),
        DIV((a, b) -> a / b, (c, b) -> c * b, (c, a) -> a / c);
        final LongBinaryOperator method;
        final LongBinaryOperator a;
        final LongBinaryOperator b;

        Operation(LongBinaryOperator method, LongBinaryOperator inverse, LongBinaryOperator b) {
            this.method = method;
            this.a = inverse;
            this.b = b;
        }
    }

    private static class Data {
        final Map<String, Long> nameToEvaluated;
        final Map<String, Expression> nameToExpression;

        Data(Map<String, Long> nameToEvaluated, Map<String, Expression> nameToExpression) {
            this.nameToEvaluated = nameToEvaluated;
            this.nameToExpression = nameToExpression;
        }

        static Data parse(String input) {
            Map<String, Long> nameToEvaluated = new HashMap<>();
            Map<String, Expression> nameToExpression = new HashMap<>();
            for (String line : input.split("\n")) {
                String[] parts = line.split(": ");
                String name = parts[0];
                if (Character.isDigit(parts[1].charAt(0))) {
                    nameToEvaluated.put(name, Long.valueOf(parts[1]));
                } else {
                    nameToExpression.put(name, Expression.parse(parts[1]));
                }
            }
            return new Data(nameToEvaluated, nameToExpression);
        }

        long reduceLHS(String knownLeft, long knownValue) {
            while (!knownLeft.equals("humn")) {
                Expression exp = nameToExpression.get(knownLeft);
                if (nameToEvaluated.containsKey(exp.a)) {
                    long a = nameToEvaluated.get(exp.a);
                    long b = exp.op.b.applyAsLong(knownValue, a);
                    nameToExpression.remove(knownLeft);
                    knownLeft = exp.b;
                    knownValue = b;
                } else if (nameToEvaluated.containsKey(exp.b)) {
                    long b = nameToEvaluated.get(exp.b);
                    long a = exp.op.a.applyAsLong(knownValue, b);
                    nameToExpression.remove(knownLeft);
                    knownLeft = exp.a;
                    knownValue = a;
                }
            }
            return knownValue;
        }

        void reduceRHS() {
            int numExpressions = nameToExpression.size();
            while (true) {
                for (Iterator<Entry<String, Expression>> itr = nameToExpression.entrySet().iterator(); itr.hasNext(); ) {
                    Entry<String, Expression> e = itr.next();
                    Expression expression = e.getValue();
                    Long a = nameToEvaluated.get(expression.a);
                    if (a != null) {
                        Long b = nameToEvaluated.get(expression.b);
                        if (b != null) {
                            long val = expression.op.method.applyAsLong(a, b);
                            nameToEvaluated.put(e.getKey(), val);
                            itr.remove();
                            break;
                        }
                    }
                }
                if (nameToExpression.size() == numExpressions) {
                    break;
                } else {
                    numExpressions = nameToExpression.size();
                }
            }
        }

    }

    private record Expression(String a, String b, Operation op) {
        static Expression parse(String input) {
            String a = input.substring(0, input.indexOf(' '));
            String b = input.substring(input.lastIndexOf(' ') + 1);
            Operation op = input.contains("+") ? Operation.ADD :
                           input.contains("-") ? Operation.SUB :
                           input.contains("*") ? Operation.MUL :
                           input.contains("/") ? Operation.DIV : null;
            return new Expression(a, b, op);
        }
    }

