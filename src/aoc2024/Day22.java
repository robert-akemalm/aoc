    private static void a(Input input) {
        long[] numbers = input.numbers;
        for (int i = 0; i < numbers.length; i++) {
            for (int generation = 0; generation < 2000; generation++) {
                numbers[i] = next(numbers[i]);
            }
        }
        System.out.println(Arrays.stream(numbers).sum());
    }

    private static void b(Input input) {
        List<Map<List<Integer>, Long>> seqToPrice = LongStream.of(input.numbers).mapToObj(Day22::sequencesAndPrices).toList();
        long sum = seqToPrice.stream()
                             .map(Map::keySet)
                             .flatMap(Collection::stream)
                             .mapToLong(seq -> seqToPrice.stream().mapToLong(s -> s.getOrDefault(seq, 0L)).sum())
                             .max().orElse(0L);
        System.out.println(sum);
    }

    private static Map<List<Integer>, Long> sequencesAndPrices(long secret) {
        Map<List<Integer>, Long> sequenceToPrice = new HashMap<>();
        List<Integer> sequence = new ArrayList<>();

        for (int i = 0; i < 2000; i++) {
            long next = next(secret);
            int diff = (int) (next % 10 - secret % 10);
            sequence.add(diff);
            if (sequence.size() > 4) {
                sequence.removeFirst();
            }

            if (sequence.size() == 4) {
                sequenceToPrice.putIfAbsent(List.copyOf(sequence), next % 10);
            }
            secret = next;
        }
        return sequenceToPrice;
    }

    private static long next(long secret) {
        secret = ((secret * 64) ^ secret) % 16777216;
        secret = ((secret / 32) ^ secret) % 16777216;
        return ((secret * 2048) ^ secret) % 16777216;
    }

