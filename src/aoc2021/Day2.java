    private static void b(String input) {
        long depth = 0;
        long x = 0;
        long aim = 0;
        for (String line : input.lines().collect(Collectors.toList())) {
            String[] parts = line.split(" ");
            int val = Integer.parseInt(parts[1]);
            switch (parts[0]) {
            case "forward" : x+= val;depth+= aim *val; break;
            case "up" : aim-= val; break;
            case "down" : aim+= val; break;
            }
        }
        System.out.println(x*depth);
    }

